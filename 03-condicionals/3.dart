// Puedes encadenar mÃºltiples condiciones usando else if.
void main() {
  int number = 5;

  if (number > 10) {
    print('The number is greater than 10');
  } else if (number > 5) {
    print('The number is greater than 5 but less than or equal to 10');
  } else {
    print('The number is 5 or less');
  }
}

